const WHATSAPP_URL = "https://wa.me/905446480648";

const getStoredLanguage = () => {
  try {
    return localStorage.getItem("miu-language");
  } catch {
    return null;
  }
};

const storeLanguage = (language) => {
  try {
    localStorage.setItem("miu-language", language);
  } catch {
    /* Local storage can be unavailable in strict browser contexts. */
  }
};

const getValue = (dictionary, path) =>
  path.split(".").reduce((value, key) => (value && value[key] !== undefined ? value[key] : undefined), dictionary);

let currentLanguage = translations[getStoredLanguage()] ? getStoredLanguage() : "tr";

const header = document.querySelector("[data-header]");
const nav = document.querySelector("[data-nav]");
const menuToggle = document.querySelector("[data-menu-toggle]");
const lightbox = document.querySelector("[data-lightbox]");
const lightboxImage = document.querySelector("[data-lightbox-image]");
const lightboxCaption = document.querySelector("[data-lightbox-caption]");
const lightboxClose = document.querySelector("[data-lightbox-close]");

const setMenuOpen = (isOpen) => {
  nav.classList.toggle("is-open", isOpen);
  menuToggle.classList.toggle("is-open", isOpen);
  menuToggle.setAttribute("aria-expanded", String(isOpen));
  menuToggle.setAttribute("aria-label", translations[currentLanguage].ui[isOpen ? "closeMenu" : "openMenu"]);
  document.body.classList.toggle("nav-open", isOpen);
};

const updateMeta = (language) => {
  const dictionary = translations[language];
  document.title = dictionary.meta.title;

  const description = document.querySelector('meta[name="description"]');
  const ogTitle = document.querySelector('meta[property="og:title"]');
  const ogDescription = document.querySelector('meta[property="og:description"]');
  const ogLocale = document.querySelector('meta[property="og:locale"]');

  if (description) description.setAttribute("content", dictionary.meta.description);
  if (ogTitle) ogTitle.setAttribute("content", dictionary.meta.title);
  if (ogDescription) ogDescription.setAttribute("content", dictionary.meta.description);
  if (ogLocale) ogLocale.setAttribute("content", language === "tr" ? "tr_TR" : "en_US");
};

const updateGalleryLabels = (language) => {
  const dictionary = translations[language];

  document.querySelectorAll("[data-gallery-index]").forEach((item) => {
    const index = Number(item.dataset.galleryIndex);
    const caption = dictionary.gallery.captions[index] || dictionary.gallery.placeholder;
    item.dataset.placeholder = dictionary.gallery.placeholder;
    item.setAttribute("aria-label", `${caption} ${dictionary.gallery.openLabel}`);
    const image = item.querySelector("img");

    if (image) {
      image.alt = caption;
    }
  });

  if (!lightbox.hidden) {
    const activeIndex = Number(lightbox.dataset.activeIndex || 0);
    lightboxCaption.textContent = dictionary.gallery.captions[activeIndex] || "";
    lightboxImage.alt = dictionary.gallery.captions[activeIndex] || "";
  }
};

const setLanguage = (language) => {
  const dictionary = translations[language] || translations.tr;
  currentLanguage = translations[language] ? language : "tr";
  document.documentElement.lang = currentLanguage;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = getValue(dictionary, element.dataset.i18n);
    if (typeof value === "string") {
      element.textContent = value;
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const value = getValue(dictionary, element.dataset.i18nPlaceholder);
    if (typeof value === "string") {
      element.setAttribute("placeholder", value);
    }
  });

  document.querySelectorAll("[data-lang]").forEach((button) => {
    const isActive = button.dataset.lang === currentLanguage;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  updateMeta(currentLanguage);
  updateGalleryLabels(currentLanguage);
  setMenuOpen(false);
  storeLanguage(currentLanguage);
};

const openLightbox = (index) => {
  const item = document.querySelector(`[data-gallery-index="${index}"]`);
  const image = item && item.querySelector("img");

  if (!image || item.classList.contains("is-missing")) return;

  const caption = translations[currentLanguage].gallery.captions[index] || image.alt;
  lightbox.dataset.activeIndex = String(index);
  lightboxImage.src = image.currentSrc || image.src;
  lightboxImage.alt = caption;
  lightboxCaption.textContent = caption;
  lightboxClose.setAttribute("aria-label", translations[currentLanguage].ui.closeGallery);
  lightbox.hidden = false;
  document.body.classList.add("lightbox-open");
  lightboxClose.focus();
};

const closeLightbox = () => {
  lightbox.hidden = true;
  lightboxImage.src = "";
  lightboxCaption.textContent = "";
  document.body.classList.remove("lightbox-open");
};

const buildWhatsAppMessage = (form) => {
  const dictionary = translations[currentLanguage].whatsapp;
  const data = new FormData(form);
  const selectedOption = form.elements.service.options[form.elements.service.selectedIndex];
  const service = selectedOption && selectedOption.value ? selectedOption.textContent.trim() : "";

  return [
    dictionary.greeting,
    `${dictionary.name}: ${data.get("name") || ""}`,
    `${dictionary.phone}: ${data.get("phone") || ""}`,
    `${dictionary.service}: ${service}`,
    `${dictionary.date}: ${data.get("date") || ""}`,
    `${dictionary.note}: ${data.get("note") || ""}`,
  ].join("\n");
};

document.querySelectorAll("[data-lang]").forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

menuToggle.addEventListener("click", () => {
  setMenuOpen(!nav.classList.contains("is-open"));
});

document.addEventListener("click", (event) => {
  if (!nav.classList.contains("is-open")) return;
  if (nav.contains(event.target) || menuToggle.contains(event.target)) return;
  setMenuOpen(false);
});

nav.querySelectorAll("a").forEach((link) => {
  link.addEventListener("click", () => setMenuOpen(false));
});

window.addEventListener("scroll", () => {
  header.classList.toggle("is-scrolled", window.scrollY > 16);
});

document.querySelectorAll(".gallery-item img").forEach((image) => {
  image.addEventListener("error", () => {
    const item = image.closest(".gallery-item");
    if (!item) return;
    item.classList.add("is-missing");
    image.hidden = true;
  });
});

document.querySelectorAll("[data-gallery-index]").forEach((item) => {
  item.addEventListener("click", () => openLightbox(Number(item.dataset.galleryIndex)));
});

lightbox.addEventListener("click", (event) => {
  if (event.target === lightbox) {
    closeLightbox();
  }
});

lightboxClose.addEventListener("click", closeLightbox);

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape") {
    setMenuOpen(false);
    if (!lightbox.hidden) {
      closeLightbox();
    }
  }
});

document.querySelector("[data-booking-form]").addEventListener("submit", (event) => {
  event.preventDefault();
  const message = buildWhatsAppMessage(event.currentTarget);
  window.open(`${WHATSAPP_URL}?text=${encodeURIComponent(message)}`, "_blank", "noopener");
});

const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("is-visible");
        revealObserver.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.15 }
);

document.querySelectorAll(".reveal").forEach((element) => revealObserver.observe(element));

setLanguage(currentLanguage);
